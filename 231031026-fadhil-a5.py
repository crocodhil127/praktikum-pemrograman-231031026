i = 0
while i < 5:
    print('Menjalankan program!')
    i += 1

"""
1.) "i = 0" Inisialisasi variabel i dengan nilai 0. Ini digunakan sebagai penghitung berapa kali kode akan dicetak

2.) "while i < 5" Ini adalah syntax loop while. Selama nilai i kurang dari 5, blok kode di dalam loop akan dijalankan. Jika kondisi ini tidak terpenuhi, program akan keluar dari loop.

3.) "print('Menjalankan program!')" Ini adalah aksi yang akan dilakukan apabila kondisi masih bernilai true

4.) "i += 1" nilai i akan ditambahkan 1. Ini adalah langkah  untuk memastikan bahwa loop tidak menjadi loop tak terbatas (infinite loop). Dengan penambahan ini, nilai i akan meningkat setiap kali program dijalankan.
"""